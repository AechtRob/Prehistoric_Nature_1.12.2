		public WorldGenAbstractTree getRandomTreeFeature(Random rand)
		{
			if (rand.nextInt(36) == 0) {
				return DEAD_TREE;
			}
			int selector = rand.nextInt(16);
			switch (selector) {
				case 0:
					return PODOCARP_TREE;
				case 1:
					return PODOCARP_TREE;
				case 2:
					return PODOCARP_TREE;
				case 3:
					return PODOCARP_TREE;
				case 4:
					return BRACHYPHYLLUM_TREE;
				case 5:
					return BRACHYPHYLLUM_TREE;
				case 6:
					return COLUMNARIS_TREE;
				case 7:
					return COLUMNARIS_TREE;
				case 8:
					return COLUMNARIS_TREE;
				case 9:
					return CYPRESS_TREE;
				case 10:
					return CYPRESS_TREE;
				case 11:
					return PARARAUCARIA_TREE;
				case 12:
					return NULL_TREE;
				case 13:
					return SILVER_TREEFERNS;
				case 14:
					return BLACK_TREEFERNS;
				case 15:
					return NULL_TREE;
			}
			return NULL_TREE;
		}

