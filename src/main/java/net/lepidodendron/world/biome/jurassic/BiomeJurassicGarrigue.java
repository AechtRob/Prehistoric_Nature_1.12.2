		public WorldGenAbstractTree getRandomTreeFeature(Random rand)
		{
			if (rand.nextInt(12) != 0) {
				return NULL_TREE;
			}
			else if (rand.nextInt(8) == 0) {
				return PARARAUCARIA_TREE;
			}
			else if (rand.nextInt(8) == 0) {
				return ANCIENT_ARAUCARIA_TREE;
			}
			else if (rand.nextInt(8) == 0) {
				return BUNYA_TREE;
			}
			return PODOCARP_TREE;

		}

